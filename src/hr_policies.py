def get_company_names():
    """Return list of available company names."""
    return list(COMPANY_DATA.keys())

def get_company_info(company_name):
    """Return complete information about a company."""
    return COMPANY_DATA.get(company_name, None)

def get_company_context(company_name):
    """Return formatted context string for the company."""
    company_data = COMPANY_DATA.get(company_name)
    if not company_data:
        return ""
    
    context = f"Company Information for {company_name}:\n"
    context += company_data["company_info"] + "\n\n"
    
    context += "HR Policies:\n"
    for policy_name, policy_content in company_data["hr_policies"].items():
        context += f"\n{policy_name}:\n{policy_content}\n"
    
    return context 